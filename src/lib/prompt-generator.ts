function pickSlot(userIndex: number, slotName: string, slotIndex: number): string {
    const options = SLOTS[slotName];
    if (!options) return slotName;
    const prime = PRIMES[slotIndex % PRIMES.length];
    const idx = ((userIndex * prime) + slotIndex * 3) % options.length;
    return options[idx];
}

function fillTemplate(template: string, userIndex: number): string {
    let result = template;
    let slotIdx = 0;
    const slotPattern = /\{(\w+)\}/g;
    let match;
    const replacements: { full: string; value: string }[] = [];

    while ((match = slotPattern.exec(template)) !== null) {
        const slotName = match[1];
        const value = pickSlot(userIndex, slotName, slotIdx);
        replacements.push({ full: match[0], value });
        slotIdx++;
    }

    for (const rep of replacements) {
        result = result.replace(rep.full, rep.value);
    }

    return result;
}

export function generatePrompt(
    round: number,
    step: number,
    userIndex: number,
    basePrompt: string
): string {
    const key = `${round}_${step}`;
    const config = VARIATIONS[key];

    if (!config || !config.templates || config.templates.length === 0) {
        return basePrompt;
    }

    const template = config.templates[userIndex % config.templates.length];
    return fillTemplate(template, userIndex);
}

// Convert user label (e.g. "A_01") to a unique index (1-100)
export function labelToIndex(label: string): number {
    const groupOrder: Record<string, number> = { A: 0, B: 1, C: 2, D: 3, E: 4 };
    const parts = label.split("_");
    if (parts.length !== 2) return 0;
    const groupIdx = groupOrder[parts[0]] ?? 0;
    const num = parseInt(parts[1], 10) || 1;
    return groupIdx * 20 + num;
}
